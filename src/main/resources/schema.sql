drop table TRIAL;

create table TRIAL (
  ID INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY, data VARBINARY(2000),
  LOG_SERIAL BIGINT,
  TRIAL_ID INT,
  NAME  VARCHAR(255),
  RESPONSE_HANDLE varchar(255),
  LATENCY INT
);

create table TRIAL_STIMULI (
  TRIAL_ID BIGINT,
  STIMULI  VARCHAR(255)
);

create table TRIAL_MEDIA (
  TRIAL_ID BIGINT,
  MEDIA  VARCHAR(255)
);

create table TRIAL_DATA (
  TRIAL_ID BIGINT,
  NAME varchar(255),
  VALUE varchar(255)
);

